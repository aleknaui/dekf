package dekf;

/**
 * Clase que almacena la información de una variable en la tabla de símbolos.
 * @author AleKnaui
 */
public class Variable {

	// --------------------------------------------------------------------------------
	// Atributos
	// --------------------------------------------------------------------------------
	
	private String tipo;
	
	// --------------------------------------------------------------------------------
	// Constructor 
	// --------------------------------------------------------------------------------
	
	public Variable( String tipo ){
		this.tipo = tipo;
	}
	
	// --------------------------------------------------------------------------------
	// Métodos
	// --------------------------------------------------------------------------------
	
	public String darTipo(){
		return tipo;
	}
	
}
